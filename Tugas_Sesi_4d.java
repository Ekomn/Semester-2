public class Tugas_Sesi_4d {
        public static void main(String[] args) throws Exception {

        // Eko Memen Nuryaman
        // Tugas Sesi 4.d : 1 1 1 
        // Tugas Sesi 4.d : 2 2 2 
        // Tugas Sesi 4.d : 3 3 3 

        System.out.println();
        for (int a = 1; a <= 3; a++) {
            for (int b = 1; b <= 3; b++) {
                System.out.print(a + " ");
            }
            System.out.println("");
        }

        System.out.println("");
    }
}


